<h3><center>Tacos al pastor</center></h3>
<p>	Son los tacos al pastor, la muestra de que la gastronomía mexicana está viva. 
Los tacos al pastor son de los favoritos de los mexicanos. Su origen puede ser rastreado hasta la comunidad libanesa en México, quienes habrían hecho el sincretismo entre el shawarmma árabe y el puerco enchilado. Además de fusionar la cocina mediterránea con las delicias de nuestro país, los tacos al pastor han seguido evolucionando de forma regional.</p>
