<h3><center>Tacos de canasta</center></h3>	
<p>	La cuna de los tacos de canasta se encuentra en el Estado de Tlaxcala; El taquero más antiguo de la localidad, Marcial Balderas, fue uno de los iniciadores que comenzó a vender estos tacos montando en su bicicleta una canasta llena de este delicioso aperitivo.
Aproximadamente el 80% de las familias de esta localidad viven del negocio de los tacos de canasta, por lo que puedes darte un gran gusto si visitas la región.
